# Задание 7: Функция с необязательным параметром
#
# Напишите функцию greet, которая принимает строку name и необязательный параметр greeting (по умолчанию "Hello").
# Функция должна возвращать приветствие. Укажите аннотации типов.

def greet(name: str, greeting: str = "Hello") -> str:
    return f"{greeting}, {name}!"